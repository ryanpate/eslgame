// Get theme from URL parameter
function getThemeFromURL() {
    const params = new URLSearchParams(window.location.search);
    return params.get('theme') || 'daily-life';
}

// Get current theme words
function getCurrentThemeWords() {
    const theme = getThemeFromURL();
    return THEMES[theme]?.words || DAILY_LIFE_WORDS;
}

// Get words by difficulty for current theme
function getWordsByDifficulty(difficulty) {
    const themeWords = getCurrentThemeWords();

    if (difficulty === 'all') {
        return themeWords;
    }

    const filtered = {};
    for (const [word, data] of Object.entries(themeWords)) {
        if (data.difficulty === difficulty) {
            filtered[word] = data;
        }
    }
    return filtered;
}

// Shuffle array
function shuffleArray(array) {
    const shuffled = [...array];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
}

// Get random words
function getRandomWords(words, count) {
    const wordList = Object.keys(words);
    const shuffled = shuffleArray(wordList);
    return shuffled.slice(0, Math.min(count, shuffled.length));
}

// Get difficulty from URL parameters
function getDifficultyFromURL() {
    const params = new URLSearchParams(window.location.search);
    return params.get('difficulty') || 'all';
}

// Save and load progress with theme support
function saveProgress(gameType, score) {
    const theme = getThemeFromURL();
    const key = `vocablab_${theme}_${gameType}`;
    const data = {
        score: score,
        date: new Date().toISOString(),
        theme: theme
    };
    localStorage.setItem(key, JSON.stringify(data));
}

function loadProgress(gameType) {
    const theme = getThemeFromURL();
    const key = `vocablab_${theme}_${gameType}`;
    const data = localStorage.getItem(key);
    return data ? JSON.parse(data) : null;
}

// Get theme info
function getCurrentThemeInfo() {
    const theme = getThemeFromURL();
    return THEMES[theme] || THEMES['daily-life'];
}
